    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int baris;
        baris = jTable1.getSelectedRow();

        String click;
        click=(jTable1.getModel().getValueAt(baris, 0).toString());

        String perintah;
        perintah = "Select * From pinjam Where id='"+click+"'";
        try {
            pst=conn.prepareStatement(perintah);
            rs = pst.executeQuery();
            if(rs.next()){
                String obj1;
                obj1=rs.getString("id");
                jTextField1.setText(obj1);

                String obj2;
                obj2=rs.getString("nama");
                jTextField2.setText(obj2);

                String obj3;
                obj3=rs.getString("keterangan");
                jTextField3.setText(obj3);

                String obj4;
                obj4=rs.getString("tanggal");
                jTextField4.setText(obj4);

                String obj5;
                obj5=rs.getString("besar");
                jTextField5.setText(obj5);

            }
        } catch (SQLException ex) {
            Logger.getLogger(Simpan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String perintah = "INSERT INTO pinjam(nama,keterangan,tanggal,besar) VALUE(?,?,?,?)";
        try {
            conn = Koneksi.getConnection();
            pst = conn.prepareStatement(perintah);
            pst.setString(1, jTextField2.getText());
            pst.setString(2, jTextField3.getText());
            pst.setString(3, jTextField4.getText());
            pst.setString(4, jTextField5.getText());
            pst.execute();
            showdata();
            reset();
        } catch (SQLException ex) {
            Logger.getLogger(Simpan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

