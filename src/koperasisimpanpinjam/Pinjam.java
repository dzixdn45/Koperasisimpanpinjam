    private void showdata() throws SQLException {
        String sql = "SELECT * FROM pinjam";
        try {
            pst = conn.prepareStatement(sql);
             rs = pst.executeQuery();
            tablepinjam = new DefaultTableModel();
            tablepinjam.addColumn("id pinjam");
            tablepinjam.addColumn("nama pinjam");
            tablepinjam.addColumn("keterangan");
            tablepinjam.addColumn("tanggal");
            tablepinjam.addColumn("besar");
         while(rs.next()) {
           tablepinjam.addRow(new Object[]{
           rs.getString(1),
           rs.getString(2),
           rs.getString(3),
           rs.getString(4),
           rs.getString(5)
           });
        }
        jTable1.setModel(tablepinjam);
        } catch (SQLException ex) {
            Logger.getLogger(Pinjam.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
